export function isCodeInCategory(code: string | number, categorySet: Set<string>): boolean {
  if (!code) return false;
  const strCode = String(code).trim();
  const cleanCode = strCode.replace(/^0+/, '');
  
  if (categorySet.has(strCode) || categorySet.has(cleanCode)) {
    return true;
  }
  
  return false;
}

